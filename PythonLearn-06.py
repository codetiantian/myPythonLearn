"""-------元组(列表可以任意修改一个元素，而元组不可以)-------"""

"""创建和访问一个元组"""
tuple1 = (1, 2, 3, 4, 5, 6)
print(tuple1[1])
""" 输出结果：
2
"""

"""元组拷贝"""
tuple2 = tuple1[:]
print(tuple2)
""" 输出结果：
(1, 2, 3, 4, 5, 6)
"""

"""拼接操作符"""
temp = ('骨精灵', '剑侠客', '武尊神')
temp = temp[:1] + ('飞燕女',) + temp[1:]
print(temp)
""" 输出结果：
('骨精灵', '飞燕女', '剑侠客', '武尊神')
"""

"""删除元组"""
del temp

